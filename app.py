def mmm_semantic_normalization(binary_vec):
    return np.array(binary_vec) * 1.0 - 0.5

def mmm_dimensional_cosine_similarity(vec_a, vec_b, norm_type='l2'):
    vec_a_norm = np.array(vec_a)
    vec_b_norm = np.array(vec_b)
    if norm_type == 'l1':
        norm_a = np.sum(np.abs(vec_a_norm))
        norm_b = np.sum(np.abs(vec_b_norm))
        if norm_a == 0 or norm_b == 0:
            return 0.0
        unit_a = vec_a_norm / norm_a
        unit_b = vec_b_norm / norm_b
    elif norm_type == 'l2':
        norm_a = np.linalg.norm(vec_a_norm)
        norm_b = np.linalg.norm(vec_b_norm)
        if norm_a == 0 or norm_b == 0:
            return 0.0
        unit_a = vec_a_norm / norm_a
        unit_b = vec_b_norm / norm_b
    elif norm_type == 'linf':
        norm_a = np.max(np.abs(vec_a_norm))
        norm_b = np.max(np.abs(vec_b_norm))
        if norm_a == 0 or norm_b == 0:
            return 0.0
        unit_a = vec_a_norm / norm_a
        unit_b = vec_b_norm / norm_b
    else:
        raise ValueError("norm_type must be 'l1', 'l2', or 'linf'")
    cos_sim = np.dot(unit_a, unit_b)
    return cos_sim

def mmm_context_dependent_similarity(user_features, car_vector, budget_weight=0.3):
    user_mask = np.array(user_features, dtype=float)
    active_indices = np.where(user_mask == 1)[0]
    if len(active_indices) == 0:
        return 0.0
    user_active = np.zeros(42)
    car_active = np.zeros(42)
    for i in active_indices:
        user_active[i] = 0.5
        car_val = car_vector[i]
        car_active[i] = (2 * car_val - 1) * 0.5
    for i in range(20, 26):
        if user_mask[i] == 1:
            user_active[i] *= budget_weight
            car_active[i] *= budget_weight
    cos_l1 = mmm_dimensional_cosine_similarity(user_active, car_active, 'l1')
    cos_l2 = mmm_dimensional_cosine_similarity(user_active, car_active, 'l2')
    sim = np.mean([cos_l1, cos_l2])
    num_matches = np.sum(car_active[active_indices] > 0)
    num_active = len(active_indices)
    logging.debug(f"Active dims: {len(active_indices)}, Matches: {num_matches}/{num_active}, Sim: {sim}")
    return sim

def build_feature_detail_cards(car_name, primary_priority, secondary_priorities, purpose_list):
    """
    Build structured feature detail cards for the result page.
    Returns a list of dicts: {icon, label, detail, is_primary}
    """
    cards = []
    car_feat = car_feature_details.get(car_name, {})

    # Primary priority card
    if primary_priority and primary_priority in car_feat:
        cards.append({
            "icon": priority_icons.get(primary_priority, "🎯"),
            "label": primary_priority,
            "detail": car_feat[primary_priority],
            "is_primary": True,
        })

    # Secondary priority cards
    for sp in [p for p in secondary_priorities if p]:
        if sp and sp in car_feat and sp != primary_priority:
            cards.append({
                "icon": priority_icons.get(sp, "✅"),
                "label": sp,
                "detail": car_feat[sp],
                "is_primary": False,
            })

    # Purpose cards to fill up to max 3 total, excluding 通勤・通学
    purpose_map = purpose_detail_map
    for purpose in purpose_list:
        if len(cards) >= 3:
            break
        if purpose == "通勤・通学":
            continue
        purpose_details = purpose_map.get(purpose, {})
        if car_name in purpose_details:
            cards.append({
                "icon": "🎯",
                "label": purpose,
                "detail": purpose_details[car_name],
                "is_primary": False,
            })

    return cards[:3]

